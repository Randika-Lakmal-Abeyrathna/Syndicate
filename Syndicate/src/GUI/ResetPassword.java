void resetPassword() {
        String opw = new String(txt_oldPassword.getPassword());
        String cpw = new String(txt_confirmPassword.getPassword());
        String un = lbl_username.getText();

        try {
            ResultSet rs = new JDBC().getData("SELECT user_name, password FROM login WHERE user_name='" + un + "' AND password='" + opw + "'");

            if (rs.next()) {
                String usn = rs.getString("user_name");
                String pw = rs.getString("password");

                if (un.equals(usn) && opw.equals(pw)) {
                    new JDBC().putData("UPDATE login SET password='" + cpw + "' WHERE user_name='" + un + "'");
                    JOptionPane.showMessageDialog(this, "Password Successfully changed.\nPlease login again.");
                }
                this.dispose();
                home.dispose();
                new Login.Login().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect password.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
