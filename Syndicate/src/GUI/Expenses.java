    private void saveToDb() {
        try {
            if (TxtArea_ExpenceDscription.getText().isEmpty() || txt_ExpenceAmount.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Fields Cannot Be Empty!");
                if (txt_ExpenceAmount.getText().isEmpty()) {
                    txt_ExpenceAmount.grabFocus();
                }

            } else {
                String description = TxtArea_ExpenceDscription.getText().toString();
//                Double amount = Double.parseDouble(txt_ExpenceAmount.getText());

                if (JOptionPane.showConfirmDialog(rootPane, "Are You Sure?", "SAVE", JOptionPane.YES_NO_CANCEL_OPTION) == 0) {
                    db.putData("INSERT INTO expence(expence_id,amount,date,description,expence_type) VALUES  "
                            + "('" + Integer.parseInt(txt_expnceID.getText()) + "','" + txt_ExpenceAmount.getText() + "','" + TheDate + "',"
                            + "'" + description + "','" + expence_type + "')");

                    JOptionPane.showMessageDialog(rootPane, "Successfully Saved");
                    log.info("Expence ID : " + txt_expnceID.getText() + " Expense Added at : " + new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()));
                }
            }

        } catch (Exception e) {
            System.out.println("Inside SaveToDb() in Expences" + e);
            e.printStackTrace();
        }
    }
    //--End of saving expences to the Database--//

    //--Load data from expences to the Table--//
    private void loadTblExpenses() {
        tcl.clearTable(tbl_ExpenceDetails);
        clearFields();
        try {
            ResultSet rs = db.getData("SELECT * FROM expence WHERE expence_type = '" + expence_type + "'");
            while (rs.next()) {
                String id = rs.getString("expence_id");
                String amount = rs.getString("amount");
                String day = rs.getString("date");
                String desc = rs.getString("description");

                tcl.addToTable(tbl_ExpenceDetails, id, desc, amount, day);
            }
        } catch (Exception e) {
            System.out.println("Inside loadTblExpenses() in Expences" + e);
        }
    }
    //--End of Load data from expences to the Table--//

