    private void txt_ExpenceAmountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_ExpenceAmountKeyTyped
        if (Character.isDigit(evt.getKeyChar())) {
            if (!txt_ExpenceAmount.getText().equals("")) {
                if (txt_ExpenceAmount.getText().contains(".")) {
                    if (i1 >= 2) {
                        evt.consume();
                    } else {
                        i1++;
                    }
                } else {
                    i1 = 0;
                }
            }

        } else if (evt.getKeyChar() == '.') {
            if (txt_ExpenceAmount.getText().contains(".")) {
                evt.consume();
            }
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_txt_ExpenceAmountKeyTyped

