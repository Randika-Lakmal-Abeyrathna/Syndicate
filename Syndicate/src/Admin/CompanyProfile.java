    private void loadDetails() {
        try {
            ResultSet rs = db.getData("SELECT * FROM admin_panel");

            while (rs.next()) {
                String email = rs.getString("company_email");
                String fixed = rs.getString("company_office");
                String mobile = rs.getString("company_mobile");

                txt_email.setText(email);
                txt_fixed.setText(fixed);
                txt_mobile.setText(mobile);
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERROR - COP 0001 \nPlease contact Development Team", "ERROR", JOptionPane.ERROR_MESSAGE);
            logError.error(ex + " at : " + new SimpleDateFormat("yyyy-MM-dd  HH:mm:ss").format(new Date()));
        }
    }

    private void updateDetails() {
        try {
            db.putData("UPDATE admin_panel SET company_email='" + txt_email.getText() + "', company_office='" + txt_fixed.getText() + "', company_mobile='" + txt_mobile.getText() + "'");
            txt_fixed.setEnabled(false);
            txt_mobile.setEnabled(false);
            txt_email.setEnabled(false);
            loadDetails();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "ERROR - COP 0002 \nPlease contact Development Team", "ERROR", JOptionPane.ERROR_MESSAGE);
            logError.error(ex + " at : " + new SimpleDateFormat("yyyy-MM-dd  HH:mm:ss").format(new Date()));
        }
    }
