void securityQuestions() {
        String un = txt_username.getText();

        try {
            ResultSet rs1 = new Classes.JDBC().getData(
                    "SELECT\n"
                    + "  login.user_name,\n"
                    + "  teacher_reg.status,\n"
                    + "  recovery.quaction1,\n"
                    + "  recovery.quaction2\n"
                    + "FROM\n"
                    + "  login\n"
                    + "INNER JOIN\n"
                    + "  recovery\n"
                    + "INNER JOIN\n"
                    + "  teacher_reg\n"
                    + "ON (login.recovery_id=recovery.recovery_id\n"
                    + "    AND\n"
                    + "    login.system_id=teacher_reg.teacher_id)\n"
                    + "WHERE\n"
                    + "  user_name='" + un + "'");

            ResultSet rs2 = new Classes.JDBC().getData(
                    "SELECT\n"
                    + "  login.user_name,\n"
                    + "  employee_reg.status,\n"
                    + "  recovery.quaction1,\n"
                    + "  recovery.quaction2\n"
                    + "FROM\n"
                    + "  login\n"
                    + "INNER JOIN\n"
                    + "  recovery\n"
                    + "INNER JOIN\n"
                    + "  employee_reg\n"
                    + "ON (login.recovery_id=recovery.recovery_id\n"
                    + "    AND\n"
                    + "    login.system_id=employee_reg.emp_id)\n"
                    + "WHERE\n"
                    + "  user_name='" + un + "'");

            ResultSet rs3 = new Classes.JDBC().getData(
                    "SELECT\n"
                    + "  login.user_name,\n"
                    + "  recovery.quaction1,\n"
                    + "  recovery.quaction2\n"
                    + "FROM\n"
                    + "  login\n"
                    + "INNER JOIN\n"
                    + "  recovery\n"
                    + "ON\n"
                    + "  (login.recovery_id=recovery.recovery_id)\n"
                    + "WHERE\n"
                    + "  user_name='" + un + "'");

            if (rs1.next()) {
                String u = rs1.getString("user_name");
                String q1 = rs1.getString("quaction1");
                String q2 = rs1.getString("quaction2");
                String s = rs1.getString("status");

                int checkStatus = Integer.parseInt(s);

                if (checkStatus == 0) {
                    JOptionPane.showMessageDialog(this, "Your account has been deactivated.\nPlease contact your admin.");
                } else if (checkStatus == 1) {
                    if (u.equals(un)) {
                        lbl_question1.setText(q1);
                        lbl_question2.setText(q2);
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect Username.");
                    }
                }
            } else if (rs2.next()) {
                String u = rs2.getString("user_name");
                String q1 = rs2.getString("quaction1");
                String q2 = rs2.getString("quaction2");
                String s = rs2.getString("status");

                int checkStatus = Integer.parseInt(s);

                if (checkStatus == 0) {
                    JOptionPane.showMessageDialog(this, "Your account has been deactivated.\nPlease contact your admin.");
                } else if (checkStatus == 1) {
                    if (u.equals(un)) {
                        lbl_question1.setText(q1);
                        lbl_question2.setText(q2);
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect Username.");
                    }
                }
            } else if (rs3.next()) {
                String u = rs3.getString("user_name");
                String q1 = rs3.getString("quaction1");
                String q2 = rs3.getString("quaction2");

                if (u.equals(un)) {
                    lbl_question1.setText(q1);
                    lbl_question2.setText(q2);
                } else {
                    JOptionPane.showMessageDialog(this, "Incorrect Username.");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void nextCheck() {
        String un = txt_username.getText();

        try {
            ResultSet rs = new Classes.JDBC().getData("SELECT login.user_name, "
                    + "recovery.answar1,"
                    + "recovery.answar2 FROM login INNER JOIN recovery "
                    + "ON (login.recovery_id=recovery.recovery_id) WHERE user_name='" + un + "'");

            if (rs.next()) {
                u = rs.getString("user_name");
                String a1 = rs.getString("answar1");
                String a2 = rs.getString("answar2");
                String geta1 = txt_question1.getText();
                String geta2 = txt_question2.getText();

                if (a1.equals(geta1) && a2.equals(geta2)) {
                    Classes.Commons.FadeOut(this);
                    this.dispose();
                    new SendMail().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "One or more answers are incorrect.");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
