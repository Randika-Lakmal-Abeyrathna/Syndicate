    void resetPassword(){
        try {
            String un = lbl_username.getText();
            String pw = new String(txt_passwordOne.getPassword());
            
            new JDBC().putData("UPDATE login SET password='"+pw+"' WHERE user_name='"+un+"'");
            JOptionPane.showMessageDialog(this, "Password Successfully changed.\nPlease login again.");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
